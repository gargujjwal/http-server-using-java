package com.ujjwalgarg.httpserver;

/**
 * The RequestMethod enum represents the HTTP methods that can be used in a
 * request.
 * Each enum constant corresponds to a standard HTTP method.
 */
public enum RequestMethod {
    GET,
    HEAD,
    POST,
    PUT,
    DELETE,
    CONNECT,
    OPTIONS,
    TRACE,
    PATCH
}
